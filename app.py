# ==================== 主畫面 ====================
tab1, tab2, tab3 = st.tabs(["📅 今日建議", "📖 所有菜式", "🍲 煲湯水"])

# ==================== Tab 1: 今日建議 ====================
with tab1:
    st.header("📅 今日建議")

    if st.button("🔄 給我一個菜式建議", use_container_width=True):
        suggested = random.choice(ALL_RECIPES)
        st.session_state.suggested = suggested

    if "suggested" in st.session_state:
        r = st.session_state.suggested

        st.success(f"**{r['name']}**")
        col1, col2 = st.columns(2)
        with col1:
            st.write(f"**烹調時間**：{r['time']}")
        with col2:
            st.write(f"**難度**：{r['difficulty']}")

        st.write("### 材料")
        for ing in r['ingredients']:
            st.write(f"- {ing}")

        st.write("### 烹調步驟")
        for i, step in enumerate(r['steps'], 1):
            st.write(f"**{i}.** {step}")

        if r.get('tips'):
            st.info(f"💡 小貼士：{r['tips']}")
    else:
        st.info("點擊上面嘅按鈕，App 會隨機畀你一個詳細食譜")

